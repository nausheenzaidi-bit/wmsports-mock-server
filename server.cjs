function extractQueryFields(queryText) {
  try {
    const body = queryText.replace(/^[^{]*\\{/, '').replace(/\\}[^}]*$/, '');
    const inner = body.replace(/^[^{]*\\{/, '').replace(/\\}[^}]*$/, '');
    return inner.split(/[\\s,]+/).map(f => f.trim()).filter(f => f && !f.startsWith('{') && !f.startsWith('}') && !f.includes('(') && !f.includes(':'));
  } catch(_) { return []; }
}

