// GraphQL-aware mock data generator using schema types
function generateMockFromGraphQLType(typeName, types, spec, depth = 0, visited = new Set()) {
  if (depth > 4) return null;
  if (!typeName || !types[typeName]) return null;
  if (visited.has(typeName)) return null; // Prevent circular references
  
  visited.add(typeName);
  
  const typeFields = types[typeName];
  if (!typeFields || typeof typeFields !== 'object') return null;

  const obj = {};
  
  for (const [fieldName, fieldType] of Object.entries(typeFields)) {
    const fieldTypeName = fieldType;
    const isScalar = SCALAR_TYPES.has(fieldTypeName);
    const isArray = fieldTypeName.startsWith('[');
    
    if (isScalar) {
      // Generate scalar values
      obj[fieldName] = generateScalarMockValue(fieldName, fieldTypeName);
    } else if (isArray) {
      // Extract inner type from [Type] or [Type]!
      const innerType = fieldTypeName.replace(/[\[\]!]/g, '');
      const innerValue = generateMockFromGraphQLType(innerType, types, spec, depth + 1, new Set(visited));
      obj[fieldName] = innerValue ? [innerValue] : [];
    } else {
      // Recursive object type
      const nestedValue = generateMockFromGraphQLType(fieldTypeName, types, spec, depth + 1, new Set(visited));
      obj[fieldName] = nestedValue;
    }
  }
  
  visited.delete(typeName);
  return obj;
}

