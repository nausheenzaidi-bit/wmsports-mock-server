function detectSchemaType(schema) {
  if (!schema) return 'unknown';

  const s = schema.trim();

  // OpenAPI (check first, before generic JSON)
  if (s.includes('openapi') || s.includes('paths:') || s.includes('"openapi"') || s.includes('"paths"')) return 'openapi';

  // GraphQL SDL
  if (s.includes('type ') || s.includes('Query')) return 'graphql';

  // Plain type definitions (nested JSON with type names/values, or "key: type" format)
  // These should be auto-convertible, not rejected as "raw JSON"
  if (isValidJSON(s)) {
    try {
      const parsed = JSON.parse(s);
      // Check if this looks like a type definition (has type names) vs raw data
      // Type definitions will be handled by detectAndConvertPlainType
      const result = detectAndConvertPlainType(s, 'AutoGenerated');
      if (result) return 'plain-types';
    } catch (e) { /* not plain types */ }
  }

  // Only reject as "raw JSON" if it's valid JSON but NOT a schema or type definition
  if (isValidJSON(s)) return 'json';

  return 'unknown';
}

function unwrapGqlType(typeNode) {
  if (!typeNode) return { name: 'Unknown', isList: false };
  if (typeNode.kind === 'NonNullType') { const r = unwrapGqlType(typeNode.type); r.required = true; return r; }
  if (typeNode.kind === 'ListType') { const inner = unwrapGqlType(typeNode.type); inner.isList = true; return inner; }
  if (typeNode.kind === 'NamedType') return { name: typeNode.name.value, isList: false };
  return { name: 'Unknown', isList: false };
}

function gqlTypeStr(typeNode) {
  if (!typeNode) return 'String';
  if (typeNode.kind === 'NonNullType') return gqlTypeStr(typeNode.type) + '!';
  if (typeNode.kind === 'ListType') return '[' + gqlTypeStr(typeNode.type) + ']';
  if (typeNode.kind === 'NamedType') return typeNode.name.value;
  return 'String';
}

const queryArgsMap = {};

function loadSchemaFiles() {
  const artifactsDir = path.join(__dirname, 'artifacts');
  if (!fs.existsSync(artifactsDir)) return;

  const schemaFiles = fs.readdirSync(artifactsDir).filter(f => f.endsWith('.graphql'));
  for (const file of schemaFiles) {
    try {
      const content = fs.readFileSync(path.join(artifactsDir, file), 'utf-8');

      const svcMatch = content.match(/^#\s*microcksId:\s*(.+?)\s*:\s*/m);
      const svcName = svcMatch ? svcMatch[1].trim() : null;
      if (svcName && !serviceRichTypeMap[svcName]) serviceRichTypeMap[svcName] = {};

      const doc = gqlParse(content);
      for (const def of doc.definitions) {
        if (def.kind === Kind.OBJECT_TYPE_DEFINITION || def.kind === Kind.OBJECT_TYPE_EXTENSION) {
          const typeName = def.name.value;
          if (!fullTypeMap[typeName]) {
            fullTypeMap[typeName] = { name: typeName, fields: [] };
          }
          if (!richTypeMap[typeName]) {
            richTypeMap[typeName] = {};
          }
          for (const field of (def.fields || [])) {
            const fname = field.name.value;
            if (!fullTypeMap[typeName].fields.includes(fname)) {
              fullTypeMap[typeName].fields.push(fname);
            }
            const unwrapped = unwrapGqlType(field.type);
            richTypeMap[typeName][fname] = unwrapped;

            if (svcName) {
              if (!serviceRichTypeMap[svcName][typeName]) serviceRichTypeMap[svcName][typeName] = {};
              serviceRichTypeMap[svcName][typeName][fname] = unwrapped;
            }
          }

          if (typeName === 'Query' || typeName === 'Mutation') {
            for (const field of (def.fields || [])) {
              const unwrapped = unwrapGqlType(field.type);
              queryFieldMap[field.name.value] = { returnType: unwrapped.name, method: typeName === 'Query' ? 'QUERY' : 'MUTATION' };
              if (svcName) queryServiceMap[field.name.value] = svcName;
              const args = [];
              for (const arg of (field.arguments || [])) {
                const aType = unwrapGqlType(arg.type);
                args.push({ name: arg.name.value, typeName: aType.name, isList: aType.isList, typeStr: gqlTypeStr(arg.type) });
              }
              if (args.length > 0) queryArgsMap[field.name.value] = args;
            }
          }
        }
      }
    } catch (_) {}
  }
  console.log(`  Schema: ${Object.keys(fullTypeMap).length} types loaded from ${schemaFiles.length} files`);
  console.log(`  Per-service maps: ${Object.keys(serviceRichTypeMap).length} services`);
}

const SCALAR_TYPES = new Set(['String', 'Int', 'Float', 'Boolean', 'ID', 'DateTime', 'Date', 'JSON', 'Long', 'BigDecimal', 'URL', 'URI']);

function serverBuildFieldsQuery(typeName, depth = 0, visited = new Set(), svcTypeMap = null) {
  const typeMap = svcTypeMap || richTypeMap;
  if (depth > 2 || !typeMap[typeName] || visited.has(typeName)) return null;
  visited.add(typeName);
  const fields = typeMap[typeName];
  const parts = [];
  const maxFields = depth === 0 ? 25 : 10;
  const entries = Object.entries(fields).slice(0, maxFields);
  
  for (const [fname, typeInfo] of entries) {
    if (fname.startsWith('_')) continue;
    if (SCALAR_TYPES.has(typeInfo.name) || !typeMap[typeInfo.name]) {
      parts.push(fname);
    } else if (depth < 1) {
      const nested = serverBuildFieldsQuery(typeInfo.name, depth + 1, new Set(visited), svcTypeMap);
      if (nested) {
        parts.push(fname + ' { ' + nested + ' }');
      }
    }
  }
  return parts.length > 0 ? parts.join(' ') : null;
}

