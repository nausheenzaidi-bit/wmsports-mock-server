function detectSchemaType(schema) {
  if (!schema) return 'unknown';

  const s = schema.trim();

  // OpenAPI (check first, before generic JSON)
  if (s.includes('openapi') || s.includes('paths:') || s.includes('"openapi"') || s.includes('"paths"')) return 'openapi';

  // GraphQL SDL (type Query, type Mutation, type SomeName {, etc.)
  if (s.includes('type ') || s.includes('Query') || s.includes('query ')) return 'graphql';

  // Plain type definitions: { name: String, age: Int } or key: type format
  // Be lenient - if it looks like JSON or key-value pairs, try to convert it
  if (s.startsWith('{') || s.includes(':')) {
    try {
      const result = detectAndConvertPlainType(s, 'AutoGenerated');
      if (result) {
        console.log('✓ Detected plain-types from:', s.substring(0, 50));
        return 'plain-types';
      }
    } catch (e) { 
      console.log('⚠️ detectAndConvertPlainType error (will try json):', e.message);
    }
  }

  // Valid JSON that's not a schema/type definition
  if (isValidJSON(s)) return 'json';

  return 'unknown';
}

function parseGraphQLSchema(schemaText) {
  const doc = parse(schemaText);
  const types = {};
  const operations = [];

  for (const def of doc.definitions) {
    const isTypelike = def.kind === Kind.OBJECT_TYPE_DEFINITION || def.kind === Kind.OBJECT_TYPE_EXTENSION
      || def.kind === 'InterfaceTypeDefinition' || def.kind === 'InterfaceTypeExtension';

    if (isTypelike) {
      const typeName = def.name.value;
      if (!types[typeName]) types[typeName] = {};
      for (const field of (def.fields || [])) {
        types[typeName][field.name.value] = unwrapGqlType(field.type);
      }

      if (typeName === 'Query' || typeName === 'Mutation') {
        for (const field of (def.fields || [])) {
          const retType = unwrapGqlType(field.type);
          const args = (field.arguments || []).map(a => ({
            name: a.name.value,
            type: unwrapGqlType(a.type),
          }));
          operations.push({
            name: field.name.value,
            method: typeName === 'Query' ? 'QUERY' : 'MUTATION',
            returnType: retType.name,
            isList: retType.isList,
            args,
          });
        }
      }
    }

    if (def.kind === 'UnionTypeDefinition') {
      const typeName = def.name.value;
      if (!types[typeName]) types[typeName] = {};
    }
  }

  // Merge implementing types' fields into interfaces/unions so the full
  // field set is available for query building and LLM prompts
  for (const def of doc.definitions) {
    if ((def.kind === Kind.OBJECT_TYPE_DEFINITION || def.kind === Kind.OBJECT_TYPE_EXTENSION) && def.interfaces) {
      for (const iface of def.interfaces) {
        const ifaceName = iface.name.value;
        if (types[ifaceName] && def.fields) {
          for (const field of def.fields) {
            if (!types[ifaceName][field.name.value]) {
              types[ifaceName][field.name.value] = unwrapGqlType(field.type);
            }
          }
        }
      }
    }
  }

  // For union types, merge fields from member types
  for (const def of doc.definitions) {
    if (def.kind === 'UnionTypeDefinition' && def.types) {
      const unionName = def.name.value;
      if (!types[unionName]) types[unionName] = {};
      for (const memberRef of def.types) {
        const memberFields = types[memberRef.name.value];
        if (memberFields) {
          for (const [fname, ftype] of Object.entries(memberFields)) {
            if (!types[unionName][fname]) types[unionName][fname] = ftype;
          }
        }
      }
    }
  }

  // Extract enum values
  const enums = {};
  for (const def of doc.definitions) {
    if (def.kind === 'EnumTypeDefinition') {
      enums[def.name.value] = (def.values || []).map(v => v.name.value);
    }
  }

  const scalars = new Set();
  const inputs = {};
  for (const def of doc.definitions) {
    if (def.kind === 'ScalarTypeDefinition') {
      scalars.add(def.name.value);
    }
    if (def.kind === 'InputObjectTypeDefinition') {
      inputs[def.name.value] = {};
      for (const field of (def.fields || [])) {
        inputs[def.name.value][field.name.value] = unwrapGqlType(field.type);
      }
    }
  }

  return { types, operations, enums, scalars, inputs };
}

function validateGraphQLSchema(schemaText) {
  const warnings = [];
  const errors = [];

  try {
    const { types, enums, operations, scalars, inputs } = parseGraphQLSchema(schemaText);
    
    const builtInScalars = new Set(['String', 'Int', 'Float', 'Boolean', 'ID']);
    const definedTypes = new Set([...Object.keys(types), ...Object.keys(enums), ...Object.keys(inputs), ...scalars, ...builtInScalars]);

    // Check all field type references
    for (const [typeName, fields] of Object.entries(types)) {
      for (const [fieldName, fieldType] of Object.entries(fields)) {
        // fieldType is { name, isList, isRequired, isNonNullList }
        const baseType = fieldType.name;
        
        if (!definedTypes.has(baseType)) {
          errors.push({
            type: 'UNDEFINED_TYPE',
            message: `Field '${typeName}.${fieldName}' references undefined type '${baseType}'`,
            field: `${typeName}.${fieldName}`,
            referencedType: baseType,
            available: Array.from(definedTypes).sort(),
          });
        }
      }
    }

    // Check operation return types
    for (const op of operations) {
      if (!definedTypes.has(op.returnType)) {
        errors.push({
          type: 'UNDEFINED_OPERATION_RETURN',
          message: `Operation '${op.name}' returns undefined type '${op.returnType}'`,
          operation: op.name,
          returnType: op.returnType,
          available: Array.from(definedTypes).sort(),
        });
      }

      // Check argument types
      for (const arg of op.args) {
        if (!definedTypes.has(arg.type.name)) {
          warnings.push({
            type: 'UNDEFINED_ARGUMENT_TYPE',
            message: `Operation '${op.name}' argument '${arg.name}' has undefined type '${arg.type.name}'`,
            operation: op.name,
            argument: arg.name,
            argumentType: arg.type.name,
          });
        }
      }
    }

    // Check for empty or minimal schema
    if (Object.keys(types).length === 0 && Object.keys(enums).length === 0) {
      warnings.push({
        type: 'EMPTY_SCHEMA',
        message: 'Schema contains no types or enums',
      });
    }

    // Check for Query type existence
    if (!types.Query && !types.Mutation && !types.Subscription) {
      errors.push({
        type: 'NO_ROOT_TYPE',
        message: 'Schema must define at least one of: Query, Mutation, or Subscription',
      });
    }

    return {
      valid: errors.length === 0,
      errors,
      warnings,
      summary: {
        typeCount: Object.keys(types).length,
        enumCount: Object.keys(enums).length,
        operationCount: operations.length,
        errorCount: errors.length,
        warningCount: warnings.length,
      },
    };
  } catch (e) {
    const parseErrors = [{
      type: 'PARSE_ERROR',
      message: `Failed to parse schema: ${e.message}`,
    }];
    return {
      valid: false,
      errors: parseErrors,
      warnings: [],
      summary: {
        typeCount: 0,
        enumCount: 0,
        operationCount: 0,
        errorCount: parseErrors.length,
        warningCount: 0,
      },
    };
  }
}

function cleanSchemaForMocking(schemaText) {
  let s = schemaText;

  s = s.replace(/^#\s*microcksId:.*$/m, '');

  s = s.replace(/extend\s+schema[\s\S]*?(?=\n(?:directive|type|enum|union|interface|input|scalar)\b)/m, '');
  s = s.replace(/^schema\s+@link[\s\S]*?\}\s*/m, '');

  const fedNames = 'key|external|requires|provides|extends|inaccessible|shareable|override|tag|link|cacheControl';

  s = s.replace(new RegExp('directive\\s+@(?:' + fedNames + ')\\s*\\([\\s\\S]*?\\)\\s*(?:repeatable\\s+)?on\\s+[A-Z_|\\s]+', 'g'), '');
  s = s.replace(new RegExp('directive\\s+@(?:' + fedNames + ')\\s+(?:repeatable\\s+)?on\\s+[A-Z_|\\s]+', 'g'), '');

  function stripDirectiveUsage(text) {
    let result = '';
    let i = 0;
    while (i < text.length) {
      const atIdx = text.indexOf('@', i);
      if (atIdx === -1) { result += text.slice(i); break; }
      result += text.slice(i, atIdx);
      const rest = text.slice(atIdx);
      const nameMatch = rest.match(new RegExp('^@(?:' + fedNames + ')(?=\\s|\\(|$)'));
      if (!nameMatch) { result += '@'; i = atIdx + 1; continue; }
      let j = nameMatch[0].length;
      let afterName = rest.slice(j);
      const parenStart = afterName.search(/\S/);
      if (parenStart >= 0 && afterName[parenStart] === '(') {
        j += parenStart + 1;
        let depth = 1;
        let inStr = false;
        let pos = j;
        while (pos < rest.length && depth > 0) {
          const ch = rest[pos];
          if (inStr) { if (ch === '"' && rest[pos - 1] !== '\\\\') inStr = false; }
          else if (ch === '"') inStr = true;
          else if (ch === '(') depth++;
          else if (ch === ')') depth--;
          pos++;
        }
        j = pos;
      }
      i = atIdx + j;
    }
    return result;
  }

  s = stripDirectiveUsage(s);

  s = s.replace(/^\s*scalar\s+_FieldSet\b.*$/gm, '');
  s = s.replace(/^\s*scalar\s+_Any\b.*$/gm, '');

  s = s.replace(/^extend\s+type\b/gm, 'type');

  const customScalars = new Set();
  const builtins = new Set(['String', 'Int', 'Float', 'Boolean', 'ID', 'Query', 'Mutation', 'Subscription']);
  const scalarRe = /(?::\s*\[?\s*)(\w+)/g;
  let m;
  while ((m = scalarRe.exec(s)) !== null) {
    if (SCALAR_TYPES.has(m[1]) && !builtins.has(m[1])) customScalars.add(m[1]);
  }

  const existingScalars = new Set();
  const existingRe = /^\s*scalar\s+(\w+)/gm;
  while ((m = existingRe.exec(s)) !== null) existingScalars.add(m[1]);

  let scalarBlock = '';
  for (const sc of customScalars) {
    if (!existingScalars.has(sc)) scalarBlock += `scalar ${sc}\n`;
  }

  if (!s.includes('enum CacheControlScope')) {
    s = s.replace(/\bCacheControlScope\b/g, 'String');
  }

  s = s.replace(/\n{3,}/g, '\n\n');

  return scalarBlock + s;
}

async function generateMockViaGraphQLTools(schemaText, ops, types, samplesPerOp) {
  const cleaned = cleanSchemaForMocking(schemaText);
  const schema = makeExecutableSchema({ typeDefs: cleaned });

  const { types: parsedTypes, enums: parsedEnums } = parseGraphQLSchema(schemaText);

  const mocks = {
    Date: () => faker.date.recent().toISOString().split('T')[0],
    DateTime: () => faker.date.recent().toISOString(),
    JSON: () => ({ key: 'value' }),
    Long: () => Math.floor(Math.random() * 1000000),
    BigDecimal: () => +(Math.random() * 1000).toFixed(2),
    URL: () => 'https://sports.example.com/' + faker.lorem.slug(),
    URI: () => 'https://sports.example.com/' + faker.lorem.slug(),
    _FieldSet: () => 'id',
    _Any: () => ({ __typename: 'Mock', id: '1' }),
    ID: () => crypto.randomUUID(),
    Int: () => faker.number.int({ min: 0, max: 1000 }),
    Float: () => +(Math.random() * 100).toFixed(2),
    Boolean: () => Math.random() > 0.5,
    String: () => faker.lorem.words(2),
  };

  for (const [typeName, fields] of Object.entries(parsedTypes)) {
    if (typeName === 'Query' || typeName === 'Mutation' || typeName === 'Subscription') continue;
    const scalarFields = {};
    for (const [fieldName, typeInfo] of Object.entries(fields)) {
      if (SCALAR_TYPES.has(typeInfo.name) || parsedEnums[typeInfo.name]) {
        scalarFields[fieldName] = typeInfo;
      }
    }
    if (Object.keys(scalarFields).length > 0) {
      mocks[typeName] = () => {
        const obj = {};
        for (const [fieldName, typeInfo] of Object.entries(scalarFields)) {
          obj[fieldName] = generateMockValue(fieldName, typeInfo, parsedTypes, parsedEnums, 3);
        }
        return obj;
      };
    }
  }

  const mockedSchema = addMocksToSchema({ schema, mocks });
  const results = {};

  function dummyArgValue(argType, depth = 0) {
    const t = argType.name;
    if (t === 'String') return '"mock"';
    if (t === 'Int') return '1';
    if (t === 'Float' || t === 'BigDecimal') return '1.0';
    if (t === 'Boolean') return 'true';
    if (t === 'ID') return '"mock-id"';
    if (parsedEnums[t]) return parsedEnums[t][0];
    if (types[t] && depth < 2) {
      const fields = types[t];
      const parts = Object.entries(fields).map(([fname, ftype]) => {
        const v = ftype.isList ? `[${dummyArgValue(ftype, depth + 1)}]` : dummyArgValue(ftype, depth + 1);
        return `${fname}: ${v}`;
      });
      return `{${parts.join(', ')}}`;
    }
    return '"mock"';
  }

  for (const op of ops) {
    const fieldSelection = buildFieldsForType(types, op.returnType, 0);
    if (!fieldSelection) continue;

    let argStr = '';
    if (op.args && op.args.length > 0) {
      const argParts = op.args.map(a => {
        const val = a.type.isList ? `[${dummyArgValue(a.type)}]` : dummyArgValue(a.type);
        return `${a.name}: ${val}`;
      });
      argStr = `(${argParts.join(', ')})`;
    }

    const keyword = op.method === 'MUTATION' ? 'mutation' : 'query';
    const queryStr = `${keyword} { ${op.name}${argStr} { ${fieldSelection} } }`;

    const examples = [];
    for (let i = 0; i < samplesPerOp; i++) {
      const result = await executeGraphQL({ schema: mockedSchema, source: queryStr });
      if (result.data && result.data[op.name] != null) {
        examples.push(result.data[op.name]);
      }
    }
    if (examples.length > 0) results[op.name] = examples;
  }

  return results;
}

function parseOpenAPISpec(specText) {
  let spec;
  try {
    spec = JSON.parse(specText);
  } catch (_) {
    spec = yaml.load(specText);
  }
  if (!spec || !spec.paths) throw new Error('Invalid OpenAPI spec: no paths found');

  const serviceName = spec.info?.title || 'RESTService';
  const serviceVersion = spec.info?.version || '1.0';
  const operations = [];

  for (const [pathKey, methods] of Object.entries(spec.paths)) {
    for (const [method, opDef] of Object.entries(methods)) {
      if (['get', 'post', 'put', 'patch', 'delete'].indexOf(method) === -1) continue;
      const opName = `${method.toUpperCase()} ${pathKey}`;
      const successCode = Object.keys(opDef.responses || {}).find(c => c.startsWith('2')) || '200';
      const successResp = (opDef.responses || {})[successCode] || {};
      const contentType = successResp.content ? Object.keys(successResp.content)[0] : 'application/json';
      const respSchema = successResp.content?.[contentType]?.schema || {};

      const reqBody = opDef.requestBody?.content?.['application/json']?.schema || null;

      operations.push({
        name: opName,
        operationId: opDef.operationId || opName,
        method: method.toUpperCase(),
        path: pathKey,
        responseCode: parseInt(successCode),
        responseSchema: respSchema,
        requestSchema: reqBody,
        parameters: opDef.parameters || [],
        summary: opDef.summary || '',
      });
    }
  }
  return { spec, serviceName, serviceVersion, operations };
}

function resolveSchemaRef(ref, spec) {
  if (!ref || !ref.startsWith('#/')) return {};
  const parts = ref.replace('#/', '').split('/');
  let result = spec;
  for (const p of parts) { result = result?.[p]; }
  return result || {};
}

function describeSchema(schema, spec, depth = 0, indent = '') {
  if (depth > 6) return '...';
  if (!schema) return 'any';
  if (schema.$ref) schema = resolveSchemaRef(schema.$ref, spec);
  if (!schema) return 'any';

  if (schema.enum) {
    return `enum(${schema.enum.slice(0, 5).join('|')}${schema.enum.length > 5 ? '|...' : ''})`;
  }

  const typeStr = (s) => {
    let t = s.type || 'any';
    if (s.format) t += `(${s.format})`;
    return t;
  };

  if (schema.type === 'object' || schema.properties) {
    const inner = indent + '  ';
    const fields = Object.entries(schema.properties || {}).map(([k, v]) => {
      if (v.$ref) v = resolveSchemaRef(v.$ref, spec);
      if (!v) return `${inner}${k}: any`;
      if (v.type === 'object' || v.properties) {
        return `${inner}${k}: ${describeSchema(v, spec, depth + 1, inner)}`;
      }
      if (v.type === 'array' && v.items) {
        let items = v.items.$ref ? resolveSchemaRef(v.items.$ref, spec) : v.items;
        if (items && (items.type === 'object' || items.properties)) {
          return `${inner}${k}: [${describeSchema(items, spec, depth + 1, inner)}]`;
        }
        return `${inner}${k}: ${typeStr(items || {})}[]`;
      }
      if (v.enum) return `${inner}${k}: ${describeSchema(v, spec, depth + 1, inner)}`;
      return `${inner}${k}: ${typeStr(v)}`;
    });
    return `{\n${fields.join(',\n')}\n${indent}}`;
  }

  if (schema.type === 'array' && schema.items) {
    let items = schema.items.$ref ? resolveSchemaRef(schema.items.$ref, spec) : schema.items;
    if (items && (items.type === 'object' || items.properties)) {
      return `[${describeSchema(items, spec, depth + 1, indent)}]`;
    }
    return `${typeStr(items || {})}[]`;
  }

  return typeStr(schema);
}

