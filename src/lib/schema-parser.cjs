function detectAndConvertPlainType(input, fallbackServiceName) {
  try {
    const trimmed = input.trim();

    // Pattern 1: Simple "key: type" lines (e.g. "name: string\nage: INT")
    const simpleLines = trimmed.split('\n').map(l => l.trim()).filter(l => l && !l.startsWith('#') && !l.startsWith('//'));
    const simpleKV = /^(\w+)\s*:\s*(string|int|integer|float|number|boolean|bool|date|datetime|id|uuid)$/i;
    const isSimpleKV = simpleLines.length > 0 && simpleLines.every(l => simpleKV.test(l));

    // Pattern 2: JSON object - flat type values OR nested structures
    let jsonObj = null;
    let isNestedJSON = false;
    if (trimmed.startsWith('{') && !/"openapi"|"swagger"|"paths"|"type"\s*:\s*"object"/.test(trimmed)) {
      try {
        // Try direct JSON parse first
        const parsed = JSON.parse(trimmed);
        const vals = Object.values(parsed);
        if (vals.length > 0) {
          // Check if it's simple flat type definitions (all strings matching type names)
          const isFlat = vals.every(v => typeof v === 'string' && /^(string|int|integer|float|number|boolean|bool|date|datetime|id|uuid)$/i.test(v));
          console.log('📦 Pattern 2 JSON parsed successfully - isFlat:', isFlat, 'vals:', vals);
          if (isFlat) {
            jsonObj = parsed;
          } else {
            // Otherwise, treat it as nested JSON structure (actual data shape)
            jsonObj = parsed;
            isNestedJSON = true;
          }
        }
      } catch (e) {
        console.log('⚠️ Pattern 2 JSON parse failed, trying lenient:', e.message.substring(0, 50));
        // Try lenient parsing: handle both { name: String } and { "name": String } formats
        // 1. Remove ALL whitespace including Unicode line/paragraph separators
        let normalized = trimmed
          .replace(/[\n\r\u2028\u2029\s]/g, '') // Remove all whitespace including Unicode separators
          .replace(/([{,])/g, '$1 ') // Add space after { and ,
          .trim();
        // 2. Remove spaces around colons: "name" : "String" → "name":"String"
        normalized = normalized.replace(/\s*:\s*/g, ':');
        // 3. Quote unquoted keys: {name: or ,name: → {"name": or ,"name":
        normalized = normalized.replace(/([{,])([a-zA-Z_]\w*):/g, '$1"$2":');
        // 4. Quote unquoted type values: :String, or :String} → :"String", or :"String"}
        normalized = normalized.replace(/:([a-zA-Z_]\w*)([,}])/g, ':"$1"$2');
        console.log('Lenient normalized:', normalized);
        try {
          const parsed = JSON.parse(normalized);
          const vals = Object.values(parsed);
          console.log('📦 Lenient parse succeeded - vals:', vals);
          if (vals.length > 0 && vals.every(v => typeof v === 'string' && /^(string|int|integer|float|number|boolean|bool|date|datetime|id|uuid)$/i.test(v))) {
            jsonObj = parsed;
            console.log('✓ Detected as type definitions');
          } else if (vals.length > 0) {
            // Even if not all are type names, treat as nested data structure
            jsonObj = parsed;
            isNestedJSON = true;
            console.log('✓ Treating as nested JSON structure');
          }
        } catch (e2) { 
          console.log('❌ Lenient parse failed:', e2.message.substring(0, 100));
          console.log('Attempted JSON:', normalized.substring(0, 200));
        }
      }
    }

    // Pattern 3: Simple GraphQL-like type (no directives, no enums, no interfaces)
    const hasGraphQLFeatures = /@\w+|^enum\s|^interface\s|^union\s|^input\s|^scalar\s|^extend\s|^schema\s|^directive\s/m.test(trimmed);
    const isGraphQLType = !hasGraphQLFeatures && /^type\s+\w+\s*\{/m.test(trimmed) && !/type\s+(Query|Mutation)\s*\{/m.test(trimmed);

    if (!isSimpleKV && !jsonObj && !isGraphQLType) return null;

    let fields = {};
    let typeName = fallbackServiceName.replace(/API$/i, '') || 'Item';
    let componentSchemas = {};

    const typeMap = {
      'string': 'string', 'int': 'integer', 'integer': 'integer',
      'float': 'number', 'number': 'number', 'boolean': 'boolean', 'bool': 'boolean',
      'date': 'string', 'datetime': 'string', 'id': 'string', 'uuid': 'string',
    };
    const formatMap = {
      'date': 'date', 'datetime': 'date-time', 'id': 'uuid', 'uuid': 'uuid',
    };

    // Helper: infer schema from value
    const inferSchema = (val, keyName = '') => {
      if (typeof val === 'string') {
        const lower = val.toLowerCase();
        if (/^(string|int|integer|float|number|boolean|bool|date|datetime|id|uuid)$/i.test(val)) {
          const schema = { type: typeMap[lower] || 'string' };
          if (formatMap[lower]) schema.format = formatMap[lower];
          return schema;
        }
        // Literal string (e.g., "ncaaf") → enum
        return { type: 'string', enum: [val] };
      }
      if (typeof val === 'number') return { type: 'number' };
      if (typeof val === 'boolean') return { type: 'boolean' };
      if (val === null) return { type: 'string' };
      if (Array.isArray(val)) {
        return {
          type: 'array',
          items: val.length > 0 ? inferSchema(val[0]) : { type: 'string' }
        };
      }
      if (typeof val === 'object') {
        const nestedSchemaName = keyName ? keyName.charAt(0).toUpperCase() + keyName.slice(1) : 'NestedObject';
        const nestedProps = {};
        for (const [k, v] of Object.entries(val)) {
          nestedProps[k] = inferSchema(v, k);
        }
        componentSchemas[nestedSchemaName] = { type: 'object', properties: nestedProps };
        return { $ref: `#/components/schemas/${nestedSchemaName}` };
      }
      return { type: 'string' };
    };

    if (isSimpleKV) {
      // Simple key: type format
      for (const line of simpleLines) {
        const m = line.match(simpleKV);
        if (m) {
          const key = m[1], rawType = m[2].toLowerCase();
          fields[key] = { type: typeMap[rawType] || 'string' };
          if (formatMap[rawType]) fields[key].format = formatMap[rawType];
        }
      }
    } else if (jsonObj) {
      if (isNestedJSON) {
        // Nested JSON structure - infer schema from actual data
        for (const [key, val] of Object.entries(jsonObj)) {
          fields[key] = inferSchema(val, key);
        }
      } else {
        // Flat type definitions
        for (const [key, rawType] of Object.entries(jsonObj)) {
          const rt = rawType.toLowerCase();
          fields[key] = { type: typeMap[rt] || 'string' };
          if (formatMap[rt]) fields[key].format = formatMap[rt];
        }
      }
    } else if (isGraphQLType) {
      const typeMatch = trimmed.match(/type\s+(\w+)\s*\{([^}]+)\}/);
      if (typeMatch) {
        typeName = typeMatch[1];
        const body = typeMatch[2];
        const fieldRe = /(\w+)\s*:\s*(\[?\w+!?\]?)/g;
        let fm;
        while ((fm = fieldRe.exec(body)) !== null) {
          const fname = fm[1];
          let gqlType = fm[2].replace(/[!\[\]]/g, '').toLowerCase();
          const gqlMap = { 'string': 'string', 'int': 'integer', 'float': 'number', 'boolean': 'boolean', 'id': 'string', 'date': 'string', 'datetime': 'string' };
          fields[fname] = { type: gqlMap[gqlType] || 'string' };
          if (gqlType === 'id') fields[fname].format = 'uuid';
          if (gqlType === 'date' || gqlType === 'datetime') fields[fname].format = 'date-time';
          if (fm[2].startsWith('[')) {
            fields[fname] = { type: 'array', items: { type: gqlMap[gqlType] || 'string' } };
          }
        }
      }
    }

    if (Object.keys(fields).length === 0) return null;

    // Add an 'id' field if not present
    if (!fields.id) {
      fields = { id: { type: 'string', format: 'uuid' }, ...fields };
    }

    const svcName = fallbackServiceName || typeName + 'API';
    const pathBase = '/' + typeName.toLowerCase();
    const refSchema = typeName;

    // Build component schemas
    const schemas = {
      [refSchema]: { type: 'object', properties: fields },
      ...componentSchemas
    };

    const openApiSpec = {
      openapi: '3.0.0',
      info: { title: svcName, version: '1.0', description: `Auto-generated CRUD API for ${typeName}` },
      paths: {
        [pathBase]: {
          get: {
            operationId: `getAll${typeName}s`,
            summary: `List all ${typeName}s`,
            responses: { '200': { description: 'OK', content: { 'application/json': { schema: { type: 'array', items: { $ref: `#/components/schemas/${refSchema}` } } } } } },
          },
          post: {
            operationId: `create${typeName}`,
            summary: `Create a new ${typeName}`,
            requestBody: { content: { 'application/json': { schema: { $ref: `#/components/schemas/${refSchema}` } } } },
            responses: { '201': { description: 'Created', content: { 'application/json': { schema: { $ref: `#/components/schemas/${refSchema}` } } } } },
          },
        },
        [`${pathBase}/{id}`]: {
          get: {
            operationId: `get${typeName}ById`,
            summary: `Get ${typeName} by ID`,
            parameters: [{ name: 'id', in: 'path', required: true, schema: { type: 'string' } }],
            responses: { '200': { description: 'OK', content: { 'application/json': { schema: { $ref: `#/components/schemas/${refSchema}` } } } } },
          },
          put: {
            operationId: `update${typeName}`,
            summary: `Update ${typeName}`,
            parameters: [{ name: 'id', in: 'path', required: true, schema: { type: 'string' } }],
            requestBody: { content: { 'application/json': { schema: { $ref: `#/components/schemas/${refSchema}` } } } },
            responses: { '200': { description: 'OK', content: { 'application/json': { schema: { $ref: `#/components/schemas/${refSchema}` } } } } },
          },
          delete: {
            operationId: `delete${typeName}`,
            summary: `Delete ${typeName}`,
            parameters: [{ name: 'id', in: 'path', required: true, schema: { type: 'string' } }],
            responses: { '204': { description: 'Deleted' } },
          },
        },
      },
      components: {
        schemas,
      },
    };

    return { openApiSpec, typeName, serviceName: svcName };
  } catch (err) {
    console.error('detectAndConvertPlainType threw error:', err.message);
    throw err;
  }
}

function convertJsonToOpenAPI(json, serviceName = 'GeneratedAPI') {
  const inferSchema = (obj) => {
    if (Array.isArray(obj)) {
      return {
        type: 'array',
        items: inferSchema(obj[0] || {})
      };
    }

    if (obj !== null && typeof obj === 'object') {
      const properties = {};
      for (const key in obj) {
        properties[key] = inferSchema(obj[key]);
      }
      return { type: 'object', properties };
    }

    if (typeof obj === 'string') return { type: 'string' };
    if (typeof obj === 'number') return { type: 'number' };
    if (typeof obj === 'boolean') return { type: 'boolean' };

    return { type: 'string' };
  };

  const schema = inferSchema(json);

  return {
    openapi: '3.0.0',
    info: {
      title: serviceName,
      version: '1.0.0'
    },
    paths: {
      '/data': {
        get: {
          summary: 'Get data',
          responses: {
            '200': {
              description: 'Successful response',
              content: {
                'application/json': {
                  schema
                }
              }
            }
          }
        }
      }
    }
  };
}

